'use client';

interface EmptyStateProps {
  icon?: React.ReactNode;
  title: string;
  message: string;
  action?: {
    label: string;
    onClick: () => void;
  };
}

export default function EmptyState({ icon, title, message, action }: EmptyStateProps) {
  const defaultIcon = (
    <svg className="w-16 h-16 text-muted-foreground" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M20 13V6a2 2 0 00-2-2H6a2 2 0 00-2 2v7m16 0v5a2 2 0 01-2 2H6a2 2 0 01-2-2v-5m16 0h-2.586a1 1 0 00-.707.293l-2.414 2.414a1 1 0 01-.707.293h-3.172a1 1 0 01-.707-.293l-2.414-2.414A1 1 0 006.586 13H4" />
    </svg>
  );

  return (
    <div className="flex flex-col items-center justify-center p-12 text-center">
      <div className="mb-4">
        {icon || defaultIcon}
      </div>
      <h3 className="text-xl font-bold text-foreground font-mono uppercase tracking-wider mb-2">
        [{title}]
      </h3>
      <p className="text-muted-foreground font-mono mb-6 max-w-md">
        {message}
      </p>
      {action && (
        <button
          onClick={action.onClick}
          className="brutalist-button bg-primary text-primary-foreground border-primary font-bold"
          style={{ padding: 'var(--space-md) var(--space-xl)' }}
        >
          [{action.label.toUpperCase()}]
        </button>
      )}
    </div>
  );
}
